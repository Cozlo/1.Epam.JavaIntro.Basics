// составить програму сравнения двух чисел 1 и 2. Если 1 меньше 2, вывести 7, в противном случае 8
package by.epam.javafundamentals.basics.bonus.branch1;

public class Branch1 {
	public static void main(String[] args) {
		int x = 1;
		int y = 2;
		if (x < y) {
			System.out.print(7);
		} else {
			System.out.print(8);
		}

	}

}
